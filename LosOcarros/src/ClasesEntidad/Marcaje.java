public class Marcaje {
    private String marcaje;
    private String nombre_tipo_marcaje;
    private String id_marcaje;

    public String getMarcaje() {
        return marcaje;
    }

    public void setMarcaje(String marcaje) {
        this.marcaje = marcaje;
    }

    public String getNombre_tipo_marcaje() {
        return nombre_tipo_marcaje;
    }

    public void setNombre_tipo_marcaje(String nombre_tipo_marcaje) {
        this.nombre_tipo_marcaje = nombre_tipo_marcaje;
    }

    public String getId_marcaje() {
        return id_marcaje;
    }

    public void setId_marcaje(String id_marcaje) {
        this.id_marcaje = id_marcaje;
    }

   
    
}
